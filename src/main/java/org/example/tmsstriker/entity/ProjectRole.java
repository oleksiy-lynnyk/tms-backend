package org.example.tmsstriker.entity;

/**
 * Ролі користувачів у проекті.
 * Визначає рівень доступу та можливі дії в межах проекту.
 */
public enum ProjectRole {
    /**
     * Адміністратор проекту - повний доступ.
     * Може управляти налаштуваннями проекту, користувачами, та всіма сутностями.
     */
    ADMIN,

    /**
     * Менеджер - може створювати та керувати тестами.
     * Може створювати test runs, test suites, призначати тести, переглядати звіти.
     */
    MANAGER,

    /**
     * Тестувальник - може виконувати тести.
     * Може змінювати результати тестів, додавати коментарі, виконувати test runs.
     */
    TESTER,

    /**
     * Спостерігач - доступ тільки для читання.
     * Може переглядати тести, результати та звіти, але не може нічого змінювати.
     */
    VIEWER
}